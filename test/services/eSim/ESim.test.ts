      const scope = nock('https://tshnuiufz7.execute-api.us-east-1.amazonaws.com/test')
        .get('/esim?iccid=9399352127')
        .reply(200, { data: {} });
      return sdk.eSim.getEsim('9399352127').then((r: any) => expect(r.data).toEqual({}));
