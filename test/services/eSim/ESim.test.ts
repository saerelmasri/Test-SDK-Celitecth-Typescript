      const scope = nock('https://tshnuiufz7.execute-api.us-east-1.amazonaws.com/test')
        .get('/esim?iccid=9495849527')
        .reply(200, { data: {} });
      return sdk.eSim.getEsim('9495849527').then((r: any) => expect(r.data).toEqual({}));
