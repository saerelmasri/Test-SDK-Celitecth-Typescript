      const scope = nock('https://tshnuiufz7.execute-api.us-east-1.amazonaws.com/test')
        .get('/esim?iccid=8024170989')
        .reply(200, { data: {} });
      return sdk.eSim.getEsim('8024170989').then((r: any) => expect(r.data).toEqual({}));
