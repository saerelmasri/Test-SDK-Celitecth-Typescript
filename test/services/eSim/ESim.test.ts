      const scope = nock('https://tshnuiufz7.execute-api.us-east-1.amazonaws.com/test')
        .get('/esim?iccid=9056462931')
        .reply(200, { data: {} });
      return sdk.eSim.getEsim('9056462931').then((r: any) => expect(r.data).toEqual({}));
