      const scope = nock('https://tshnuiufz7.execute-api.us-east-1.amazonaws.com/test')
        .get('/esim?iccid=5693285029')
        .reply(200, { data: {} });
      return sdk.eSim.getEsim('5693285029').then((r: any) => expect(r.data).toEqual({}));
