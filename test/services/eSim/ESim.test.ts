      const scope = nock('https://tshnuiufz7.execute-api.us-east-1.amazonaws.com/test')
        .get('/esim?iccid=9085386298')
        .reply(200, { data: {} });
      return sdk.eSim.getEsim('9085386298').then((r: any) => expect(r.data).toEqual({}));
